#!/usr/bin/env python3
"""
Prompt构建器 - 根据ChatGLM2 API格式优化
"""

def build_bank_contract_review_prompt(user_clause: str, retrieved_knowledge: str) -> str:
    """
    构建银行合同审查增强Prompt
    格式根据API调用示例优化
    """
    prompt = f"""【角色设定】
你是一名资深银行法律合规官，具有10年银行合同审查经验。

【审查任务】
请根据以下参考材料，严格审查待审合同条款。

【待审条款】
{user_clause}

【参考资料】
{retrieved_knowledge}

【审查要求】
1. **风险识别**：识别法律合规风险、信用风险、操作风险
2. **差异对比**：与银行标准范本逐条对比差异
3. **修改建议**：给出具体、可执行的修改文本
4. **依据引用**：每个判断必须引用参考材料中的具体条款

【输出格式】
### 风险等级：[高/中/低]
### 风险点列表：
1. [风险描述，类型，影响]
### 差异分析：
- [差异点1：原文 vs 标准条款]
### 修改建议：
[具体修改后的条款文本]
### 复核提示：
[需要人工重点复核的事项]"""
    
    return prompt